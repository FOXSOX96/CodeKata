public class CodeKata002 {


    public int solution(int num1, int num2) {
        int answer = num1 * num2;
        return answer;
    }


    public static void main(String[] args) {
        CodeKata002 code = new CodeKata002();
        System.out.println(code.solution(3, 4));
        System.out.println(code.solution(27, 19));
    }
}


